#Python Data types
x = 5
print(type(x))

#list 
x = ["apple", "banana", "cherry"]
print(type(x))

#bool 
word="the best way"
x = "way" in word
print(type(x))

#set
x = {"apple", "banana", "cherry"}
print(type(x))

#dict 
x = {"name" : "John", "age" : 36}
print(type(x))

#bytes
x = b"Hello"
print(type(x))